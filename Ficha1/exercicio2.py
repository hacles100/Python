numero = int(input('Digite um numero:'))

if numero == 1:
    print('Domingo')

elif numero == 2:
    print('Segunda')

elif numero == 3:
    print('Terca')

elif numero == 4:
    print('Quarta')

elif numero == 5:
    print('Quinta')

elif numero == 6:
    print('Sexta')

elif numero == 7:
    print('Sabado')

else:
   print('O numero tem de ser de 1 a 7')